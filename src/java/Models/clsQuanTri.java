package Models;

/**
 *
 * @author Admin
 */
public class clsQuanTri {
    int id;
    String taikhoan;
    String matkhau;
    int loai;
    int trangthai;

    public clsQuanTri() {
    }

    public clsQuanTri(int id, String taikhoan, String matkhau, int loai, int trangthai) {
        this.id = id;
        this.taikhoan = taikhoan;
        this.matkhau = matkhau;
        this.loai = loai;
        this.trangthai = trangthai;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTaikhoan() {
        return taikhoan;
    }

    public void setTaikhoan(String taikhoan) {
        this.taikhoan = taikhoan;
    }

    public String getMatkhau() {
        return matkhau;
    }

    public void setMatkhau(String matkhau) {
        this.matkhau = matkhau;
    }

    public int getLoai() {
        return loai;
    }

    public void setLoai(int loai) {
        this.loai = loai;
    }

    public int getTrangthai() {
        return trangthai;
    }

    public void setTrangthai(int trangthai) {
        this.trangthai = trangthai;
    }
    
}
